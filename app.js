require('dotenv').config();
var createError = require('http-errors');
var express = require('express');
var path = require('path');
var cookieParser = require('cookie-parser');
var logger = require('morgan');
var mongoose = require('mongoose');
var cors = require('cors');

//set environment variables
const mongodbConn = process.env.MONGOOSE;
const baseRoute = process.env.BASEROUTE;
const apiRoute = process.env.APIROUTE;

//connect mongodb to homework collection
mongoose.connect(mongodbConn, {useNewUrlParser: true, useUnifiedTopology: true});
//check a connection status
const db = mongoose.connection;
db.on('error', console.error.bind(console, 'connection error:'));
db.once('open', function() {
  // we're connected!
  console.log('Mongodb is connected!');
});

var indexRouter = require(baseRoute);
var apiRouter = require(apiRoute)

var app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'jade');

app.use(cors());
app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({extended: false}));
app.use(cookieParser());
// app.use(express.static(path.join(__dirname, 'public')));  //First looking for the default url: for local
app.use(express.static(path.join(__dirname, 'client/build')));  //First looking for the default url: for heroku
//app.use(express.static(path.join(__dirname, 'client/public')));  //First looking for the default url: for coolify

app.use('/', indexRouter);
app.use('/api', apiRouter);

//production mode needs to redirection
console.log(`NODE_ENV: ${process.env.NODE_ENV}`);
// if(process.env.NODE_ENV === 'production') {
//   app.get('*', (req, res) => {
//     res.sendFile(path.jon(__dirname + '/client/build/index.html'));
//   });
// }

// catch 404 and forward to error handler
app.use(function (req, res, next) {
  next(createError(404));
});

// error handler
app.use(function (err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
